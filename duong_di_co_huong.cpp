#include <bits/stdc++.h>
#define ll long long
#define endl "\n"
#define For(i, a, b) for (int i = a; i < b; i++)
#define Forb(i, a, b) for (int i = a; i <= b; i++)
#define mp make_pair
#define vi vector<int>
#define vll vector<ll>
#define pb push_back
#define is insert
#define pi pair<int, int>
#define vii vector<pi>
#define all(v) v.begin(), v.end()
#define ms(a) memset(a, 0, sizeof(a))
using namespace std;
vector<int> adj[1001];
bool visited[1001] = {0};
int a[1001][1001];
int parent[1001] = {0};
vector<int> path;
void dfs(int s)
{
    visited[s] = true;
    for (auto x : adj[s])
    {
        if (!visited[x])
        {
            parent[x] = s;
            dfs(x);
        }
    }
}
void bfs(int s)
{
    queue<int> qe;
    qe.push(s);
    visited[s] = true;
    while (!qe.empty())
    {
        int top = qe.front();
        qe.pop();
        for (auto x : adj[top])
        {
            if (!visited[x])
            {
                parent[x] = top;
                qe.push(x);
                visited[x] = true;
            }
        }
    }
}
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    //   freopen("input.txt","r",stdin);
    //   freopen("output.txt","w",stdout);
    ll t;
    cin >> t;
    while (t--)
    {
        int n, m, h, k;
        cin >> n >> m >> h >> k;
        for (int i = 1; i <= m; i++)
        {
            int x, y;
            cin >> x >> y;
            adj[x].pb(y);
        }
        bfs(h);
        if (visited[k] == false)
        {
            cout << -1 << endl;
        }
        else
        {
            while (h != k)
            {
                path.pb(k);
                k = parent[k];
            }
            path.pb(h);
            reverse(all(path));
            for (int i = 0; i < path.size(); i++)
            {
                if (i == path.size() - 1)
                    cout << path[i];
                else
                    cout << path[i] << " -> ";
            }
            cout << endl;
        }
        ms(visited);
        path.clear();
        ms(parent);
        for (auto &x : adj)
            x.clear();
    }
    return 0;
}
